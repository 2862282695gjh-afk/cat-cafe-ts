/** Server 退出时杀掉所有 agent 的 CLI 子进程 */
export function cleanup() {
  for (const agentId of pool.agentIds) {
    const agent = pool.get(agentId);
    if (agent && "stop" in agent && typeof agent.stop === "function") {
      agent.stop();
    }
  }
}
